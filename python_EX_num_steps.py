# Problem description

"""

PROBLEM:

You are given two arrays a1, a2, ... an and b1, b2, ... bn. 
In each step, you can set ai = ai - bi if ai >= bi. 
Determine the minimum number of steps that are required to make all a's equal.

INPUT FORMAT:

First line: n
Second line: a1, a2, ... an
Third line: b1, b2, ... bn

OUTPUT FORMAT:

Print the minimum number of steps that are required to make all a's equal. 
If it is not possible, then print -1.

CONSTRAINTS:
    
1 <= n, ai, bi <= 5000

"""

# Number of Steps (own solution)

len_arrays = int(input())
a = [int(i) for i in input().split()]
b = [int(i) for i in input().split()]
steps = 0
min_a = min(a)
flag = True


while flag:
    flag = False
    for i in range(len_arrays):
        if (a[i] > min_a) and (a[i] >= b[i]):
            a[i] = a[i] - b[i]
            steps += 1
            flag = True
        
if len(set(a)) == 1:
    print(steps)
else:
    print(-1)       

    
